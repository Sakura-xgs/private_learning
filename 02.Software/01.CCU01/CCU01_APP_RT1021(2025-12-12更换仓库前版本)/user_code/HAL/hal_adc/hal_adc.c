/**
	* 函数名: 二分查找->查温度表
  * 函数功能:查找数据在表中对应的位置  表中数据从小到大
  * 输入参数: 表长度、要查找的数据
	* 输出    :
  * 返 回 值: 数据在表中的位置
  * 说    明:
	* 方法    ：
  */
U16 HAL_TEMP_AdToTemp_PT1000(U16 TempData)
{
	U16 st,ed,Temp = 0;
	U16 TabLong = 369;
	U16 i;
    U16 fR_GUN_T=0;//由float改为U16

	st=0U;
	ed=TabLong-1U;
	i=0U;

//	* fU_GUN_T=(float)TempData*(3.3/4096);//电压
//	* fR_GUN_T = (fU_GUN_T * 10000)/(3.3*6 - fU_GUN_T);//电阻

	fR_GUN_T = (TempData*10000U)/((4096U*6U) - TempData);

	if(fR_GUN_T >= PT1000_R_Table[ed]) {return ed;}
	else if(fR_GUN_T <= PT1000_R_Table[st]) {return st;}
	else{
		//
	}

	while(st < ed)
	{
		Temp=(st+ed)/2U;

		if(fR_GUN_T == PT1000_R_Table[Temp]) {break;}
		if((fR_GUN_T < PT1000_R_Table[Temp+1U]) && (fR_GUN_T > PT1000_R_Table[Temp])) {break;}

		if(fR_GUN_T > PT1000_R_Table[Temp])
		{
			st = Temp;
		}
		else
		{
			ed = Temp;
		}

		if(i++ > TabLong) {break;}
	}

	if(st > ed){return 0;}

	if(Temp >= TabLong) {Temp = TabLong;}

	return Temp;
}

U16 HAL_TEMP_AdToTemp_3380(U16 TempData)
{
    U16 High = (TEMP_TABLE_SIZE_3380 - 1U);
    U16 Low = 0;
    U16 uc16Temp = 0;
    U16 Mid = (U16)(High + Low) / 2U;
    U16 Sequence = SEQ_RISING;

	//判断表格升降序
	if( TempCoffAdTable_3380[0] < TempCoffAdTable_3380[TEMP_TABLE_SIZE_3380 - 1U] )
	{
		Sequence = SEQ_RISING;
		//超出量程
		if( (TempData < TempCoffAdTable_3380[0]) || (TempData > TempCoffAdTable_3380[TEMP_TABLE_SIZE_3380-1U]) )
		{
			return 0;
		}
	}
	else
	{
		Sequence = SEQ_LOWER;
		//超出量程
		if( (TempData > TempCoffAdTable_3380[0]) || (TempData < TempCoffAdTable_3380[TEMP_TABLE_SIZE_3380-1U]) )
		{
			return 0;
		}
	}

	//二分查找
	for(;;)
	{
		if( TempData < TempCoffAdTable_3380[Mid] )
        {
            (Sequence==SEQ_RISING)?(High = Mid):(Low = Mid);
        }

        if( TempData > TempCoffAdTable_3380[Mid] )
        {
            (Sequence==SEQ_RISING)?(Low = Mid):(High = Mid);
        }

        if( (TempData==TempCoffAdTable_3380[Mid]) || (1U==(High-Low)) || (1U==(Low-High)) )
        {
        	 uc16Temp = (Mid * 100U);
        	 break;
        }

        Mid = (U16)((High + Low) / 2U);
	}

    return  ((uc16Temp/100U) + 20U);	/* 偏移量为-60，这里需要从-40偏移量+20变化为-60偏移量 */
}

